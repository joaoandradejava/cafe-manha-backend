package com.joaoandrade.cafemanha.domain.dto;

public class FiltroColaborador {

	private String nome;
	private String cpf;

	public FiltroColaborador() {
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

}
